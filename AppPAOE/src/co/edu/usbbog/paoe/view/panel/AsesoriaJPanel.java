    private void guardarAsesoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarAsesoriaActionPerformed
        try {
            if (!jTextFieldNumAsesoria.getText().isEmpty()
                    || !jTextFieldTipAcomp.getText().isEmpty()
                    || !jTextFieldAcceso.getText().isEmpty()
                    || !jTextFieldRemitidoPor.getText().isEmpty()
                    || !jTextFieldProblema.getText().isEmpty()
                    || !jTextFieldAcciones.getText().isEmpty()
                    || !jTextFieldAsistencia.getText().isEmpty()
                    || !jTextFieldCulminacion.getText().isEmpty()
                    || !jTextFieldRemitidoA.getText().isEmpty()
                    || !jTextFieldObservaciones.getText().isEmpty()) {
                int respuesta = JOptionPane.showConfirmDialog(this, "¿Seguro desea guardar la Asesoría?", "Confirmación de Asesoría", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (respuesta == JOptionPane.YES_OPTION) {
                    JOptionPane.showMessageDialog(null, asesoriaB.createAsesoria(Integer.parseInt(jTextFieldNumAsesoria.getText()),
                            ObtenerFecha(),
                            jTextFieldTipAcomp.getText(),
                            jTextFieldAcceso.getText(),
                            jTextFieldRemitidoPor.getText(),
                            jTextFieldProblema.getText(),
                            jTextFieldAcciones.getText(),
                            jTextFieldAsistencia.getText(),
                            jTextFieldCulminacion.getText(),
                            jTextFieldRemitidoA.getText(),
                            jTextFieldObservaciones.getText(),
                            buscarPaciente().getDocumento(),
                            inicio.darDocumentoUsuario()
                    ));
                    limpiar();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Algun campo esta vacio. Por favor llene todos los campos necesarios para registrar a un pasciente.", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Algun registro está mal escrito. Por favor compruebe todos los campos.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_guardarAsesoriaActionPerformed

