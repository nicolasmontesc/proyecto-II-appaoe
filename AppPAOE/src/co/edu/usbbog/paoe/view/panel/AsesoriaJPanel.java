    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (buscarPaciente() == null) {
            JOptionPane.showMessageDialog(this, "Error. No se encontro el pacinete.", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            jLabelNomPaciente.setText(buscarPaciente().getNombres() + " " + buscarPaciente().getApellidos());
            jLabelCodigo.setText(buscarPaciente().getCodigo());
            jLabelEdad.setText(buscarPaciente().getEdad() + "");
            jLabelFinan.setText(buscarPaciente().getFinanciacion());
            jLabelSemestre.setText(buscarPaciente().getSemestre());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

